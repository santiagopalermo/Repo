    private void verificacion(String texto, Integer textoR) {
        if (texto != "") {
            if (texto.equals("0")) {
                txtCalculadora.setText(String.valueOf(textoR));
            } else {
                    txtCalculadora.setText(txtCalculadora.getText() + String.valueOf(textoR));
            }

        }

    }

    private String limpiarPuntoFlotante(String numero) {
        String ret = numero;
        char ultima = numero.charAt(numero.length() - 1);
        char coma = numero.charAt(numero.length() - 2);
        if (ultima == '0' && coma == '.') {
            ret = numero.substring(0, numero.length() - 2);
        }

        return ret;
    }


