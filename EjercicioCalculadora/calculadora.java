    private float sumar() {
        float resultado = 0;
        for (int i = 0; i < numeros.size(); i++) {
            resultado += numeros.get(i);
        }

        return resultado;
    }

    private float restar() {
        float resultado = numeros.get(0);
        for (int i = 1; i < numeros.size(); i++) {
            resultado -= numeros.get(i);
        }

        return resultado;
    }

    private float dividir() {
        float resultado = numeros.get(0);
        for (int i = 1; i < numeros.size(); i++) {
            resultado /= numeros.get(i);
        }

        return resultado;
    }

    private float multiplicar() {
        float resultado = numeros.get(0);
        for (int i = 1; i < numeros.size(); i++) {
            resultado *= numeros.get(i);
        }

        return resultado;
    }

    private float modulo() {
        float resultado = numeros.get(0);
        for (int i = 1; i < numeros.size(); i++) {
            resultado %= numeros.get(i);
        }

        return resultado;
    }
    
    private void resolver(){
        //FALTA ver unas cosas en este método
        numeros.add(Float.parseFloat(txtCalculadora.getText()));
        float resultado = 0;
        if (sumo) {
            resultado = sumar();
            sumo = false;
        } else if (resto) {
            resultado = restar();
            resto = false;
        } else if (divido) {
            resultado = dividir();
            divido = false;
        } else if (multiplico) {
            resultado = multiplicar();
            multiplico = false;
        } else if (modulo) {
            resultado = modulo(); // esto es el resto de la división %
            modulo = false;
        }

        numeros.removeAll(numeros);
        String res = limpiarPuntoFlotante(String.valueOf(resultado));
        txtCalculadora.setText(res);
    }

